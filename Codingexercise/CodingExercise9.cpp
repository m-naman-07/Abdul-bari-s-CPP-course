/*
Number is Positive or Negative
Find if a given number is Positive or Negative
Parameter
n - a integer number
Output
cout<<"positive" or cout<<"negative"
*/
#include<iostream>

void Sign(int n)
{
    
   if(n>=0){
       std::cout << "positive";
   }
   else{
       std::cout<<"negative";
   }
    
}
